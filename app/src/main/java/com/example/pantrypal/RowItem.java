package com.example.pantrypal;

public class RowItem {
    private String name;
    private String image;

    public void setName(String name){
        this.name = name;
    }
    public void setImage(String image){
        this.image = image;
    }
    public String getImage(){
        return this.image;
    }
    public String getName(){
        return this.name;
    }

}
